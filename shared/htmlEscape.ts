/**
 * Shared HTML escaping for tracker description fields.
 * Used by frontend and backend ADO adapters — do not duplicate.
 */
export function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
